int aduna(int a, int b) {
    return a + b;
}

// Functie pentru afisare
void afiseaza(int rezultat) {
    printf("Rezultatul este: %d\n", rezultat);
}

int main() {
    // Declara variabile
    int numar1 = 5;
    int numar2 = 10;
    int suma;

    // Aduna numerele
    suma = aduna(numar1, numar2);

    // Afiseaza rezultatul
    afiseaza(suma);

    return 0;
}

